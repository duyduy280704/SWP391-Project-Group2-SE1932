package models;

/**
 *
 * @author Quang
 */
public class SetBanner {
    String id, title;
    
    byte[] image;

    public SetBanner() {
    }

    public SetBanner(String id, String title, byte[] image) {
        this.id = id;
        this.title = title;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }
    
    
}
