package models;

/**
 *
 * @author Dwight
 */
public class Event {
    private int id;
    private String title;
    private String startDate;

    public Event() {
    }

    public Event(int id, String title, String startDate) {
        this.id = id;
        this.title = title;
        this.startDate = startDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }
    
    
}
