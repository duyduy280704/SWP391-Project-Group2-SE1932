package models;

/**
 *
 * @author Quang
 */
public class SetAbout {

    String id, title, content;

    byte[] image;

    public SetAbout() {
    }

    public SetAbout(String id, String title, String content, byte[] image) {
        this.id = id;
        this.title = title;
        this.content = content;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }

}
