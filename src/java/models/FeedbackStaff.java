package models;

/**
 *
 * @author Quang
 */
public class FeedbackStaff {
    String course, student, text, date;

    public FeedbackStaff() {
    }

    public FeedbackStaff(String course, String student, String text, String date) {
        this.course = course;
        this.student = student;
        this.text = text;
        this.date = date;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getStudent() {
        return student;
    }

    public void setStudent(String student) {
        this.student = student;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    
}