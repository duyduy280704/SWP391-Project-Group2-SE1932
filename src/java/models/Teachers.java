package models;

/**
 *
 * @author Quang
 */
public class Teachers {
    String id,password,fullname,birthDate,gender,expertise,roleId,picture,email;

    public Teachers() {
    }

    public Teachers(String id, String password, String fullname, String birthDate, String gender, String expertise, String roleId, String picture, String email) {
        this.id = id;
        this.password = password;
        this.fullname = fullname;
        this.birthDate = birthDate;
        this.gender = gender;
        this.expertise = expertise;
        this.roleId = roleId;
        this.picture = picture;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getExpertise() {
        return expertise;
    }

    public void setExpertise(String expertise) {
        this.expertise = expertise;
    }

    public String getRoleId() {
        return roleId;
    }

    public void setRoleId(String roleId) {
        this.roleId = roleId;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    
}
