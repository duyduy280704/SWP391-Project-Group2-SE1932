package models;

/**
 *
 * @author Quang
 */
public class NoticeToStaff {
    String staff, mess, date;

    public NoticeToStaff() {
    }

    public NoticeToStaff(String staff, String mess, String date) {
        this.staff = staff;
        this.mess = mess;
        this.date = date;
    }

    public String getStaff() {
        return staff;
    }

    public void setStaff(String staff) {
        this.staff = staff;
    }

    public String getMess() {
        return mess;
    }

    public void setMess(String mess) {
        this.mess = mess;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    
}
